package homework.day2.cycletask;

public class CycleClass1 {
    public void printResult() {
        System.out.println("Task 1 (Через While все числа от 0 до 20 в 1 строку через пробел): ");


        int num = 0;
        while (num <= 20) {
            System.out.print(num + " ");
            num++;
        }
        System.out.println();

    }

}
