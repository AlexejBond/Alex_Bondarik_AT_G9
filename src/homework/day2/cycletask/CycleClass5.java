package homework.day2.cycletask;

public class CycleClass5 {
    public void printResult5(int[] array) {
        System.out.println("Task 5 (Массив в обратном порядке): ");
        for (int i = array.length - 1; i >= 0; i--) {
            System.out.print(array[i] + " ");
        }
        System.out.println();

    }

}
