package homework.day2.cycletask;

public class CycleClass6 {
    public void printResult6(int[] array) {
        System.out.println("Task 6 (Результат умножения каждого элемента массива на 5): ");
        for (int i = 0; i < array.length; i++) {
            int result = array[i] * 5;
            System.out.print(result + " ");
        }
        System.out.println();

    }

}
