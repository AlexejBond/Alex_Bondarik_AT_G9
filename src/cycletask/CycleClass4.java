package cycletask;

public class CycleClass4 {
    public void printResult4(int[] array) {
        System.out.println("Task 4 (Массив в обратном порядке): ");
        for (int i = 0; i < array.length; i++) {
            System.out.print(array[i] + " ");
        }
        System.out.println();

    }


}
