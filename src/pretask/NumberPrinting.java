package pretask;
//класс NumberPrinting,  используйте цикл for и выведите на экран числа от 1 до 10;
public class NumberPrinting {
    public static void main(String[] args) {
        for (int i = 10; i >= 0; --i) {
            System.out.println(i);
        }
        System.out.println("Boom !");
    }
}

